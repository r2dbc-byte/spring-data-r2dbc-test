package com.example.reactiverestservice;

import org.springframework.data.annotation.Id;

public class Person {

  @Id
  private Integer id;
  private String name;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
